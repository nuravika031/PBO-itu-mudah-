package paket10;

public class Tanaman {
    public String bunga;
    public String pohonPisang;
    public String pohonMangga;

    public String getBunga() {
        return bunga;
    }

    public void setBunga(String bunga) {
        this.bunga = bunga;
    }

    public String getPohonPisang() {
        return pohonPisang;
    }

    public void setPohonPisang(String pohonPisang) {
        this.pohonPisang = pohonPisang;
    }

    public String getPohonMangga() {
        return pohonMangga;
    }

    public void setPohonMangga(String pohonMangga) {
        this.pohonMangga = pohonMangga;
    }
    
    
}
